package Model;

/**
 *
 * @author angel
 */
public class Serie {
    int IdSerie;
    String Nombre;

    public Serie() {
    }

    public Serie(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getIdSerie() {
        return IdSerie;
    }

    public void setIdSerie(int IdSerie) {
        this.IdSerie = IdSerie;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    
}


